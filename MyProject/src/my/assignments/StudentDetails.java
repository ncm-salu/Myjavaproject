package my.assignments;

public class StudentDetails {

	    private String name;
	    private int rollNumber;
	    // Method to set & get the student's name
	    public void setName(String name)
	    {
	    	this.name=name;
	    }
	    
	    public String getName() {
	        return name;
	    }

	    // Method to set & get the student's roll number
	    public void setRollNumber(int i)
	    {
	    	this.rollNumber=i;
	    }
	    public int getRollNumber() {
	        return rollNumber;
	    }

	}

	



